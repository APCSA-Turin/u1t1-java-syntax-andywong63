/* This class shows information about me
   Name: Andy */
public class MyInfo {
    public static void main(String[] args) {
        System.out.println("My name is Andy."); // Name
        System.out.println(); // Blank line
        System.out.println("I am 15 years old."); // Age
        System.out.print("I have ");
        System.out.print(5 * 3 - 13);
        System.out.println(" brothers."); // Number of brothers
    }
}